async def generate_one(session: int, slide: int, text: str, semaphore: asyncio.Semaphore):
    """Generate a single narration audio file."""
    filename = f"session{session}_slide{str(slide).zfill(2)}.mp3"
    filepath = os.path.join(OUTPUT_DIR, filename)

    if os.path.exists(filepath) and os.path.getsize(filepath) > 0:
        print(f"  [SKIP] {filename} (already exists)")
        return

    async with semaphore:
        try:
            communicate = edge_tts.Communicate(text, VOICE)
            await communicate.save(filepath)
            size_kb = os.path.getsize(filepath) / 1024
            print(f"  [OK] {filename} ({size_kb:.0f} KB)")
        except Exception as e:
            print(f"  [ERROR] {filename}: {e}")


async def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    semaphore = asyncio.Semaphore(5)  # Run 5 concurrent TTS tasks

    tasks = []
    for session_num, narrations in NARRATIONS.items():
        print(f"\n=== Session {session_num} ({len(narrations)} slides) ===")
        for slide_idx, text in enumerate(narrations, start=1):
            tasks.append(generate_one(session_num, slide_idx, text, semaphore))

    await asyncio.gather(*tasks)

    # Count results
    total = sum(len(n) for n in NARRATIONS.values())
    generated = len([f for f in os.listdir(OUTPUT_DIR) if f.endswith('.mp3')])
    print(f"\n=== Done: {generated}/{total} audio files generated ===")


if __name__ == "__main__":
    asyncio.run(main())
